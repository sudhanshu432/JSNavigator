/** Type Coercion */

console.log(5 + 5);

let num = '5' + 5;
console.log(typeof num);

let fullName = 'Sudhanshu' + ' ' + 'Kumar';
console.log(fullName);

let sub = '55' - 10;
console.log(sub);
console.log(typeof(sub));

console.log('4' * 2);

// Implicit conversion in JavaScript, also known as type coercion, 
// occurs when the language automatically converts one data type to another during operations.