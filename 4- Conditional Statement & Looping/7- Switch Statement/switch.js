/** Switch Statement */

// Simulate a traffic light using switch

let color = 'yellow';

switch (color) {
  case 'red':
    console.log('STOP! the light is red.');
    break;

  case 'yellow':
    console.log(' CAUTION! the light is yellow.');
    break;

  case 'green':
    console.log('GO AHEAD!');
    break;

  default:
    console.log('Invalid color');
}
